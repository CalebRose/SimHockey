func GetNumericalSortValueByLetterGrade(grade string) int {
	switch grade {
	case "A+":
		return 1
	case "A":
		return 2
	case "A-":
		return 3
	case "B+":
		return 4
	case "B":
		return 5
	case "B-":
		return 6
	case "C+":
		return 7
	case "C":
		return 8
	case "C-":
		return 9
	case "D+":
		return 10
	case "D":
		return 11
	case "D-":
		return 12
	}
	return 13
}

func GetDrafteeSalary(pick, year, round uint, isSalary bool) float32 {
	var valueMap [39][4]float32
	if isSalary {
		valueMap = getSalaryMap()
	} else {
		valueMap = getBonusMap()
	}
	if pick >= 1 && pick <= 32 && year >= 1 && year <= 4 {
		return valueMap[pick-1][year-1]
	}
	return valueMap[30+round][year-1]
}

